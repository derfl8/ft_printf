#include "libftprintf.h"

int	ft_putnbr(int n)
{
	unsigned int	i;

	i = 0;
	if (n == -2147483648)
	{
		write(1, "-2147483648", 11);
		return (10);
	}
	if (n < 0)
	{
		write(1, "-", 1);
		n = n * -1;
	}
	if (n < 10)
	{
		ft_putchar(n + 48);
		i++;
	}
	if (n / 10)
	{
		ft_putnbr(n / 10);
		ft_putnbr(n % 10);
	}
	return (i);
}
