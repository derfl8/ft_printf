#include "ft_printf.h"

int	ft_puthexh(unsigned int n)
{
	unsigned int	i;

	i = 0;
	if (n / 16)
		i += ft_puthexh(n / 16);
	if (n % 16 < 10)
		i += ft_putchar((n % 16) + '0');
	else
		i += ft_putchar((n % 16) + '7');
	return (i);
}
